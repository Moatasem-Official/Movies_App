class MovieVideosEntity {
  final int id;
  final List<ResultVideoEntity> results;

  const MovieVideosEntity({required this.id, required this.results});
}

class ResultVideoEntity {
  final String iso_639_1;
  final String iso_3166_1;
  final String name;
  final String key;
  final String site;
  final int size;
  final String type;
  final bool official;
  final String publishedAt;
  final String id;

  const ResultVideoEntity({
    required this.iso_639_1,
    required this.iso_3166_1,
    required this.name,
    required this.key,
    required this.site,
    required this.size,
    required this.type,
    required this.official,
    required this.publishedAt,
    required this.id,
  });
}
